


<h1>What makes an album great?</h1>

<p>When Rolling Stone updated their greatest album list in 2020, they noted that part of the reason they undertook this task was that “no list is definitive — tastes change, new genres emerge, the history of music keeps being rewritten.” That is true. 
</p>

<p>In 2003, there were only 26 hip-hop albums on the list, the highest ranked of which was Public Enemy’s It Takes a Nation of Millions to Hold Us Back at 48, the only hip-hop album in the top 100.
</p>

